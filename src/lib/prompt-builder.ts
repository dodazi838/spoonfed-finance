// ─── 긴 보고서용 프롬프트 생성 ───
export function buildLongReportPrompt(numPages: number): string {
  return `
당신은 '떠먹여주는 금융경제'라는 서비스의 최고 등급 수석 에디터입니다.
첨부된 리포트는 ${numPages}페이지 분량의 긴 보고서입니다.
본문을 읽고 핵심 뼈대만 아래 JSON 형식에 맞게 추출하세요. (차트는 그리지 마세요)

1. summary: 보고서 전체를 관통하는 핵심 요약(최소 3개~최대 5개 문장)
2. chapters: 이 보고서의 본문 목차를 1차원 문자열 배열로 추출
   - [필수 규칙 1] 목차의 개수는 **절대 7개를 초과할 수 없습니다.** (권장 3~5개)
   - [필수 규칙 2] 만약 원본의 목차가 7개가 넘는다면, 세부 항목을 상위 개념으로 묶어 반드시 7개 이하의 대주제로 압축하세요.
   - [필수 규칙 3] 모든 챕터 제목 앞에는 반드시 "1. ", "2. "와 같이 통일된 숫자 넘버링을 붙이세요. (예: "1. 서론", "2. 본론")
   - 목차 페이지가 없으면 가장 큰 폰트의 '대제목' 위주로 스캔하여 핵심 소주제를 직접 구성하세요.
3. lifeImpact: 일반 대중의 일상에 미치는 영향 직관적 요약 (2~3문장)

군더더기 없이 오직 JSON만 반환하세요.
{
  "summary": ["요약1", "요약2"],
  "chapters": ["1. 챕터", "2. 챕터", "3. 챕터"],
  "lifeImpact": "영향...",
  "isShortReport": false
}
  `.trim();
}

